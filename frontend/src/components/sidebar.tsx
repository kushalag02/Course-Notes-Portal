import { SidebarTrigger } from "@/components/ui/sidebar";
import { Button } from "@/components/ui/button";
export function AppSidebar({ ...props }: any) {

  const { toggleSidebar, state } = useSidebar()
  return (
    <Sidebar collapsible="icon" {...props}>
      <SidebarHeader className="h-16 border-b flex flex-col items-center justify-center">
        <TeamSwitcher teams={data.teams} />
      </SidebarHeader>
      <SidebarContent>
        <NavMain items={data.navMain} role={props?.user?.role} />
        {/* <NavProjects projects={data.projects} /> */}
      </SidebarContent>
      <SidebarFooter>
        <NavUser user={props.user} />
      </SidebarFooter>
      <SidebarRail />
      <div className="absolute right-0 z-50 w-6 pl-[2px] -mr-[26px] h-6 top-5">
        <Tooltip>
          <TooltipTrigger asChild>
            <Button
              variant="ghost"
              size="icon"
              className="w-3 h-6"
              onClick={toggleSidebar}
            >
              {state === "collapsed" ? <ChevronRight /> : <ChevronLeft />}
            </Button>
          </TooltipTrigger>
          <TooltipContent
            side="right"
            align="center"
          >
            {state === "collapsed" ? <>Open</> : <>Close</>}
          </TooltipContent>
        </Tooltip>
      </div>
    </Sidebar>
  )
}


